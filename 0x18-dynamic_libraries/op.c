/**
 * add - function that adds two numbers
 * @a: number 1
 * @b: number 2
 * Return: a and b
*/
int add(int a, int b)
{
    return (a + b);
}

/**
 * sub - function that substracts two from numbers
 * @a: number 1
 * @b: number 2
 * Return: a and b
*/
int sub(int a, int b)
{
    return (a - b);
}

/**
 * mul - function that multiplies two numbers
 * @a: number 1
 * @b: number 2
 * Return: a and b
*/
int mul(int a, int b)
{
    return (a * b);
}

/**
 * div - function that divides two numbers
 * @a: number 1
 * @b: number 2
 * Return: a and b
*/
int div(int a, int b)
{
    return (a / b);
}

/**
 * mod - function find the modulo of two numbers
 * @a: number 1
 * @b: number 2
 * Return: a and b
*/
int mod(int a, int b)
{
    return (a % b);
}
